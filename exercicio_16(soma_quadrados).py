#Faça um algoritmo que leia dois valores inteiros (A e B) e apresente o resultado da soma do quadrado de cada valor lido.

#Reunião de variaveis
print(f"Calculadora de soma dos quadrados. ")
num1 = int(input("Digite um valor para o primeiro numero: "))
num2 = int(input("digite um valor para o segundo numero: "))

#Resolução
soma1 = (num1**2) + (num1**2)
soma2 = (num2**2) + (num2**2)

#Apresentação
print(f"A soma dos valores é {soma1} e {soma2}.")