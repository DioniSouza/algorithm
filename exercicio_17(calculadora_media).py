#Faça um algoritmo que leia dois números nas variáveis Val1 e Val2, calcule sua média na variável Media e imprima seu valor.

#Variaveis
print(f"Calculdora de media de dois valores.")
val1 = int(input(f"Digite o valor do primeiro numero: "))
val2 = int(input(f"Digite o valor do segundo numero: "))

#Resolução
media = (val1 + val2) / 2

#Apresentação
print(f"A média dos valores apresentados é ", media)