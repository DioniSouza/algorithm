#Faça um algoritmo que leia um valor inteiro e apresente os resultados do quadrado e do cubo do valor lido.

#Reunião de variaveis
num1 = int(input("Digite um valor do numero a ser calculado: "))

#Cálculo
quadrado = num1**2
cubo = num1**3

#Apresentação dos dados
print(f"O quadrado do numero apresentado é {quadrado} e o cubo é {cubo}.")